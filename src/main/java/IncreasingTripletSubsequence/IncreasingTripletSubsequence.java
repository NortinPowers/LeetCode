class SolutionIncreasingTripletSubsequence {
    public boolean increasingTriplet(int[] nums) {
        if (nums.length < 1 || nums.length > 5 * Math.pow(10, 5)) {
            return false;
        }
        ArrayList<Integer> ints = new ArrayList<>();
        ints.add(nums[nums.length - 1]);
        ArrayList<Integer> varInt = new ArrayList<>();
        for (int i = nums.length - 2; i >= 0; i--) {
            if (varInt.size() > 0) {
                for (int j = 0; j < varInt.size(); j++) {
                    if (nums[i] < varInt.get(j)) {
                        return true;
                    }
                }
            }
            if (ints.size() < 2 && nums[i] < ints.get(0)) {
                ints.add(nums[i]);
            }
            if (ints.size() < 2 && nums[i] > ints.get(0)) {
                ints.clear();
                ints.add(nums[i]);
            }
            if (ints.size() > 2 && nums[i] == ints.get(ints.size() - 1)) {
                continue;
            }
            if (ints.size() == 2 && nums[i] < ints.get(1)) {
                return true;
            }
            if (ints.size() == 2 && nums[i] > ints.get(1)) {
                if (nums[i] == ints.get(0)) {
                    continue;
                }
                if (nums[i] >= ints.get(0)) {
                    varInt.add(ints.get(1));
                    ints.clear();
                    ints.add(nums[i]);
                } else {
                    ints.set(1, nums[i]);
                }

            }
            try {
                if (ints.size() < 3 && nums[i + 1] == ints.get(1)) {
                    continue;
                }
            } catch (Exception e) {
            }
        }
        return false;
    }
}
